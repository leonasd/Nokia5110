//------------GPIO口设置----------------------------
void Nokia5110::IO_Config()
{
	LCD_RST_OUT;
	LCD_CE_OUT;
	LCD_DC_OUT;
	LCD_DIN_OUT;
	LCD_CLK_OUT;
}

//****************************************************************************************************
// 写一字节
//****************************************************************************************************

void Nokia5110::writeByte(unsigned char dt, unsigned char command)
{
	unsigned char i;
	LCD_CE_L;            //使能LCD
	if(command==0)            //传送命令
	   LCD_DC_L;
	else
	   LCD_DC_H;         //传送数据
	for(i=0;i<8;i++)
	{
		if(dt&0x80)
			LCD_DIN_H;
		else
			LCD_DIN_L;
		dt=dt<<1;
		LCD_CLK_L;
		LCD_CLK_H;
	}
	LCD_DC_H;
    LCD_CE_H;//关闭LCD
	LCD_DIN_H;
}
//****************************************************************************************************
// LCD初始化
//****************************************************************************************************
void Nokia5110::init(void)
{
	IO_Config();

	LCD_CE_H;
	LCD_RST_L;//产生一个让LCD复位的低电平脉冲
  	delayMicroseconds(10);
  	LCD_RST_H;
	delayMicroseconds(100);
	writeByte(0x21,0);//使用水平寻址,进入拓展指令
    writeByte(0x13, 0);//定液晶偏置系统 1:48
	writeByte(0xb8,0);//设定设置Vop,相当于亮度
							//c8 bb b0 9b
    writeByte(0x20,0);//芯片活动 使用基本指令并且水平寻址
	writeByte(0x0c,0);//设定显示模式，正常显示

	clear();
}

//---------------------------------------
//名称: 设置坐标函数
//参数：X：0－83 Y：0－5
//作者：huoenlai
//邮箱：1084170961@qq.com
//日期：2011/3/28
//-----------------------------------------
void Nokia5110::set_XY(unsigned char X, unsigned char Y)
{
    Y|=0x40;
    X|=0x80;
	writeByte(Y, 0);// 列
	writeByte(X, 0);// 行
	LCD_CE_H;
}
//****************************************************************************************************
// LCD清屏
//****************************************************************************************************

void Nokia5110::clear(void)
{
	unsigned char t,k;
	set_XY(0,0);
	for(t=0;t<6;t++)
	{
		for(k=0;k<84;k++)
		{
			writeByte(0x00,1);
		}
	}
}
//****************************************************************************************************
// 写一个字符
//****************************************************************************************************
void Nokia5110::writeChar(unsigned char X,unsigned char Y,unsigned char c)
{
	unsigned char i,j ;
	const  unsigned char *map = &font[0][0];
    c -= 32; //变换
    map += c *( HEIGHT/8*WIDTH);

    for(i=0;i<HEIGHT/8;i++)
    {
        set_XY(X, Y+i);
        for(j=0;j<WIDTH;j++)
	    writeByte(*map++, 1);
    }
}

//****************************************************************************************************
// 写英文字符串
//****************************************************************************************************
void Nokia5110::writeString_en(unsigned char X,unsigned char Y,unsigned char *s)
{
    unsigned char i=0,j=0;
    while (*s)
    {
        writeChar(X, Y,*s++);
        X+=WIDTH;
        i++;
        if(i>=(84/WIDTH))
		{
			X=0;
			Y+=HEIGHT;
			i=0;
			j++;
		}
		if(j>=(48/HEIGHT))
		{
		 return;
		}
    }
}
//****************************************************************************************************
// 写一个任意数字
//****************************************************************************************************
void Nokia5110::writeNumber(unsigned char x,unsigned char y, unsigned long number)
{
    unsigned char num[10]={""},i,j,k;
    if(number==0)                   //number 0 时候
    {
        writeChar(x, y,'0');
    }
    else
    {
        for(i=0;number>0;i++)
        {
            num[i]=lcd_display[number%10];//每位以字符表示
            number/=10;
        }
        for(j=0;j<i/2;j++){k=num[j];num[j]=num[i-1-j];num[i-1-j]=k;}
        writeString_en(x,y,num);
    }
}

/*
 * 显示一张全屏的图片
 */

void Nokia5110::writeImage(const unsigned char * cp)
{
	unsigned char i = 0, j = 0;

	for (i = 0; i < 6; i++)
	{
		set_XY(0, i);
		for (j = 0; j < 84; j++)
		{
			writeByte(*cp++, 1);
		}
	}
}


#define CMD_BUFFER_LEN 100
void Nokia5110::printf (unsigned char poX, unsigned char poY,char *fmt, ...)
{
	static char buffer[CMD_BUFFER_LEN+1]; 
    unsigned  char i = 0;
    va_list arg_ptr;
    va_start(arg_ptr, fmt);  
    vsnprintf(buffer, CMD_BUFFER_LEN+1, fmt, arg_ptr);
    writeString_en(poX,poY,(unsigned char *)buffer);
    va_end(arg_ptr);
}
